    private void fetchMovies(){
        try{
        String query="select movies.id, movies.name as Naziv, price as Cijena, start_time as Pocetak, end_time as Kraj, halls.name as Dvorana, 3D"
                + " FROM movies JOIN halls ON movies.hall_id = halls.id WHERE start_time > NOW()";

        pst=con.prepareStatement(query);
        rs=pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        jTable1.getColumnModel().getColumn(0).setMinWidth(0);
        jTable1.getColumnModel().getColumn(0).setMaxWidth(0);
        }catch(SQLException e){
           JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void fetchReservations(){
        try{
        String query="SELECT movies.name as Naziv, seat_number as 'Broj Sjedala', price as Cijena, start_time as Pocetak, end_time as Kraj, halls.name as Dvorana, 3D, IF(NOW() > end_time, \"Da\", \"Ne\") AS Zavrseno\n" +
        "FROM reservations \n" +
        "JOIN movies ON reservations.movie_id = movies.id \n" +
        "JOIN halls ON movies.hall_id = halls.id\n" +
        "WHERE user_id = ?\n" +
        "ORDER BY Kraj desc";
            
        pst=con.prepareStatement(query);
        pst.setInt(1, userId);
        rs=pst.executeQuery();
        jTable2.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(SQLException e){
           JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
