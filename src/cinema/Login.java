    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText();
        String pwd = jPasswordField1.getText();
        if (email.equals("") || pwd.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Neka polja su prazna", "Gre≈°ka", 1);
        } else {
            try {
                con = Connectionz.getConnection();
                pst = con.prepareStatement("SELECT * FROM USERS WHERE email = ? AND password=AES_ENCRYPT(?,'secretCode')");
                pst.setString(1, email);
                pst.setString(2, pwd);
                rs = pst.executeQuery();
                if (rs.next()) {
                    String s1 = rs.getString("role");
                    if (s1.equals("Admin")) {
                        Admin ad = new Admin();
                        this.setVisible(false);
                        dispose();
                        ad.setVisible(true);
                    } else if (s1.equals("Customer")) {
                        Customer ct = new Customer(rs.getInt("id"));
                        this.setVisible(false);
                        dispose();
                        ct.setVisible(true);
                    } else if (s1.equals("Superadmin")) {
                        Superadmin sa = new Superadmin();
                        this.setVisible(false);
                        dispose();
                        sa.setVisible(true);
                    }
                } else {
                    JOptionPane.showMessageDialog(this,"Unijeli ste neispravne podatke");
                }
            } catch(SQLException ex) {
                System.out.println(ex.getMessage());
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

